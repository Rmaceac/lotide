const countOnly = (allItems, itemsToCount) => {
  const countObj = {};
  for (const item of allItems) {
    if (itemsToCount[item]) {
      if (countObj[item]) {
        countObj[item] += 1;
       } else {
        countObj[item] = 1;
     }
    }
  } return countObj;
};


